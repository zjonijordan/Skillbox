students = {
    1: {
        'name': 'Bob',
        'surname': 'Vazovski',
        'age': 23,
        'interests': ['biology, swimming']
    },
    2: {
        'name': 'Rob',
        'surname': 'Stepanov',
        'age': 24,
        'interests': ['math', 'computer games', 'running']
    },
    3: {
        'name': 'Alexander',
        'surname': 'Krug',
        'age': 22,
        'interests': ['languages', 'health food']
    }
}
def calculate():
    interests = tuple()
    soname = 0
    for i, p in students.items():
        interests += tuple(p['interests'])
        for _ in p['surname']:
            soname += 1
    return interests, soname

print(calculate())
